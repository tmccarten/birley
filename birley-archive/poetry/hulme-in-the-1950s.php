    <h3 class="content-text__title content-text__title--poems">Hulme in the 1950s</h3>

    <p>Archive images confuse my memory<br>
    Of 1950s’ long streets with tall green lamp posts<br>
    Beside gas lights of an era not yet passed<br>
    And trams with overhead gables led<br>
    Where buses followed</p>

    <p>Crofts were our playing fields<br>
    Where cowboys chased Indians<br>
    And boys were boys<br>
    At war<br>
    Hop-scotch on corporation flag stones<br>
    Balls thudding against high walls<br>
    Hands felt the warmth of coal fires<br>
    Where demolition exposed brickwork<br>
    Maybe war, maybe progress.</p>

    <p>Stick kids<br>
    In unwashed clothes<br>
    Smelt like the hops dark-clouds-brew<br>
    From Moss Lane<br>
    At dusk if you stood still and quiet<br>
    You could hear the whistle then the workers<br>
    Fleeing Trafford Park factories<br>
    Souls stamping homewards</p>

    <p>Milk floats, coal drays and rag and bone men<br>
    Supplied nourishment, warmth and donkey stone<br>
    So my mother could hold her head high.<br>
    In front of neighbours<br>
    She bragged of a bathroom<br>
    Her own kitchen<br>
    And an unshared lime-washed toilet<br>
    In the back yard</p>

    <p>It was rumoured that some roomed as lodgers<br>
    Living lives of get-by existence<br>
    Cooker on the landing six families sharing<br>
    Back yard ablutions<br>
    But the Christians lived in their bought<br>
    Double fronted Jamaican home grandeur<br>
    As Delvino Street expanded the Dalrymple’s<br>
    Exploded out of their small terrace<br>
    Ignored by Prouse<br>
    Her one boy two girls’ family unit</p>

    <p>Here we lived<br>
    One Nigerian his wife a Liverpudlian and two kids<br>
    In a street called Radnor<br>
    That we called the United Nations<br>
    Of Poland, Norway, Pakistan and India<br>
    So Saturdays when Billy the cobbler<br>
    Got Guinness brave<br>
    His curses began with the English<br>
    Then circumnavigated the globe</p>

    <p>&copy;<i>SuAndi - National Black Arts Alliance - 2017</i></p>

