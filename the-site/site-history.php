      <p>The site of the Manchester Metropolitan University campus at <a href="http://www2.mmu.ac.uk/birley/" class="content-text__link">Birley</a> has seen many changes over the years. Known for a time as Birley Fields, the present site owes its name to the first Chairman of the Manchester School Board, Herbert Birley, who laid the foundation stone for its first school at <a href="schools-and-education/vine-street-school.php" class="content-text__link">Vine Street</a>.</p>
      <p>Prior to commencing the building of the campus, an <a href="<?php echo $linkArchive?>dig.php" class="content-text__link">archaeological dig</a> was undertaken which exposed the layers of just some of these changes.</p>

      <h3 id="jacksonsFarm" class="content-text__title content-text__title--plain">Jackson's Farm</h3>
      <p>The site’s origins possibly date back to medieval times in the form of Jackson Farm from which the later Upper Jackson Street presumably took its name. The location of Jackson’s Farm lies within the present Birley boundaries, and is shown on an OS map of 1848 when it comprised a farmhouse and two further buildings to the north.</p>

      <h3 id="theTerraces" class="content-text__title content-text__title--plain">The Terraces</h3>
      <p>It was not long before the area became more industrialised and over time the site was populated with streets of terrace houses built for Manchester’s mill workers, shops of all kinds and other local amenities.</p>
      <p>The area was also home to a number of workshops and small factories, including the nearby factory at Cooke Street where Henry Royce’s first car rolled off the production line in 1904, marking the beginnings with his business partner, Charles Rolls, of the illustrious Rolls-Royce.</p>
      <p>There was a strong sense of community in the Hulme terraces and former residents still talk with pride of childhood memories and an international feel to the community, particularly in 1950s and 1960s. Unfortunately, heavy bombing during World War II led to damage of a number of properties in the area. This subsequently resulted in wide-scale demolition of the terrace streets and in many cases, the dispersion of the former residents under a scheme known as the ‘slum clearances’.</p>

      <h3 id="holyTrinityChurch" class="content-text__title content-text__title--plain">Holy Trinity Church</h3>
      <p>The <a href="<?php echo $linkArchive?>dig.php" class="content-text__link">archaeological dig</a> also revealed the foundations of Holy Trinity Church which stood from Victorian Times until it was finally demolished following heavy bombing. Amongst the finds was a well-preserved head believed to be of the church patron, Eleanor Atherton. This currently resides in the university’s Special Collections.</p>

      <h3 id="deckAccess" class="content-text__title content-text__title--plain">Deck Access and Crescents</h3>
      <p>In the 1960s, regeneration and a new wave of town planning saw the construction of the initially acclaimed deck access flats of Otterburn Close between Bonsall Street and <a href="<?php echo $linkArchive?>music.php" class="content-text__link">Epping Walk</a> bridge (the latter being notable as the location of a series of iconic photographs by photographer Kevin Cummins of the band Joy Division). The ‘decks’ were long walkways which sought to emulate the former terrace streets. The flats were built around central courtyards. Whilst there were serious concerns for families, particularly in the increase in crime, safety hazards and the ‘decks’ being unpatrolled by police as they were not classed as streets, as with the terraces, life in the 1960s flats is also remembered for the times of community and mutual support.</p>
      <p>The nearby larger development of the Crescents rather fancifully sought to emulate the shape of the Georgian Crescents in Bath but later achieved notoriety for their flawed construction. In later years, the flats saw a burgeoning student and bohemian sub-culture when rents ceased to be collected and students and residents (by then there were no families) began to use the flats as ‘digs’, art studios and even makeshift night clubs. Former residents included French actor Alain Delon, Andy Warhol’s Nico and Mark Kermode.</p>

      <h3 id="birleyHigh" class="content-text__title content-text__title--plain">Birley High</h3>
      <p>A reminder of the educational tradition that continues in the name of Birley today, the eponymous Birley High School, was located close to the present site near the Hulme Hippodrome. A much loved and respected comprehensive, Birley High School welcomed its first pupils in 1967. However, as families moved away and the educational system changed the school closed and was demolished in 1999. <a href="http://www.birleyhighschool.com" class="content-text__link">Birley High</a> was an exponent of forward-thinking and inclusive education, fondly remembered by many of its former pupils. The flame red phoenix emblem of the school symbolised the rebirth and hopes of the mid-1900s regeneration of Hulme. Hulme Business Park now occupies the former site.</p>

      <h3 id="1990s" class="content-text__title content-text__title--plain">1990s and Millennium</h3>
      <p>The area underwent further redevelopment as part of regeneration of the 1990s and early millennium.</p>
      <p>Following a review of some of the less successful elements of the 1960s developments in terms of housing and infrastructure, the Moss Side and Hulme Partnership (MSHP) was established in 1997 to look at improvements in the area resulting in one of the most ambitious regeneration programmes in Britain at the time.</p>
      <p>The <a href="#" class="content-text__link">MSHP report</a> aimed to look at the benefits of Hulme and how these might support the regeneration of the area. The investigation concluded that new developments were needed to create a density of people and activity sufficient to sustain the local economy.</p>
      <p>The regeneration project had 8 main concepts which were used in helping to shape the renewed look and feel: Public Realm; Streets and Squares; Mix of Use and Tenure; Density; Links to and through Hulme; Sense of Place; Stewardship and Sustainability.</p>
      <p>As well as large-scale investment in housing stock, new road infrastructure, the business and retail parks, Hulme Park with the nearby <a href="https://radicalmanchester.wordpress.com/pages/hulme-library-mural-photos-by-arwa-aburawa/#jp-carousel-597" class="content-text__link">Hulme Mural</a> and the Hulme Arch Bridge over the Princess Parkway were all part of this more recent phase of regeneration.</p>

      <h3 id="presentDay" class="content-text__title content-text__title--plain">Present Day</h3>
      <p>Today the site is home to two of the University’s <a href="faculty-history.php" class="content-text__link">faculties</a>: the Faculty of Education and Faculty of Health, Psychology and Social Care, who have had their own fascinating part to play in the history of health and education before occupying their present home in the <a href="http://venues.mmu.ac.uk/venues/brooks-building/" class="content-text__link">Brooks Building</a>. The <a href="http://www2.mmu.ac.uk/accommodation/manchester/birley/" class="content-text__link">student residences</a> take their names from the terrace streets: Vine, Dunham, Naylor and Warde. There is a pioneering <a href="http://www2.mmu.ac.uk/birley/sustainability/sustainability-trail/" class="content-text__link">energy centre</a>, a <a href="<?php echo $linkGrow?>sustainability.php" class="content-text__link">community orchard</a>,  herb garden and a naturally retained woodland area managed by Hulme Community Garden Centre. The site sits alongside a cooperative with artists’ studios, a café and housing under Homes for Change, as well as new housing, businesses and retail outlets along Stretford Road, George Parr Road, Old Birley Street and Archway. Of the previous streets, Bonsall Street still remains and traverses the present site via a re-landscaped footbridge.</p>


