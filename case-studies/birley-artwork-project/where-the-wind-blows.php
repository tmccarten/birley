      <h3 class="content-text__title">Where The Wind Blows</h3>

      <p>Where the Wind Blows developed from a proposal by landscape architect, <a href="http://www.msa.ac.uk/staff/profile/ifisher" class="content-text__link">Ian Fisher</a>: ‘Reconnecting with Natural Processes’ for 10-metre high windsocks on a central lawn. Along with Ian’s own practice, structural engineers, <a href="http://www.renaissance-ltd.com/" class="content-text__link">Renaissance</a> and windsock supplier <a href="https://www.windsocks.direct/" class="content-text__link">Windsocks Direct</a> developed and tested the viability of the proposals.</p>
      <p>Further consultation was initiated with researchers in Science and Engineering and the university’s biodiversity group in investigating opportunities for weather monitoring.
      The design itself evolved into a collaboration between locally based sculptor Joseph Ayavoro, Director of <a href="https://creativehandsfoundation.org/" class="content-text__link">Creative Hands</a>, and textile artist <a href="http://www.art.mmu.ac.uk/profile/akettle" class="content-text__link">Alice Kettle</a>. Alice created the textile design of the windsocks, whilst Joseph designed the drawings for a wave like compass at its base indicating the winds that led to an arrival to/or departure from the site, through the arrival of those who passed through the site, and made the area their home either permanently or temporarily.</p>
      <p>A poem by SuAndi: <a href="../../birley-archive/poetry/hulme-in-the-1950s.php" class="content-text__link">Hulme in the 1950s</a>, which connected the project to the Terraces, completed the project.</p>
      <p>This is a summary of the project development showing the collaborative intent integral to Ian’s proposal and practice:</p>

      <h4>Project Development Summary</h4>
      <div class="serif-text">
        <p>Having developed from an initial concept design where the grid of terrace houses was planted over to form a grid of trees, landscape architect Ian Fisher, developed the designs for this installation project based on the following elements:</p>
        <p>Measurement – in responding to the original ‘grid’ theme and the sustainability brief, the project aimed to act as an indicator of changes in weather patterns through monitoring equipment on the 10-metre high windsock poles. Possibilities for monitoring included wind speed, rainwater and humidity. Researchers in the School of the Environment and members of the Environment Team were consulted in this process.</p>
        <p>Compass – continuing the theme of measurement, the compass acted as a direct way of grounding the tall ‘masts’ of the windsock poles in a specific context and place. The compass is also reminiscent of navigation and the many routes and cultures that form a specific time and place, connecting the past with present, and the present with future possibilities and directions. Using his sculptural knowledge, <a href="http://www.josephayavoro.com/" class="content-text__link">Joseph Ayavoro</a>, drew up a compass design along the patterns of waves in galvanised steel. Joseph’s work draws on Nigerian heritage and his own architectural background.
        The Masts – 10-metre high windsock poles are intended to be relative to the size of the concrete columns that frame the entrance of the Brooks Building so that the structure does not diminish in dimensions but instead creates a distinctive feature.</p>
        <p>Sculptural research was undertaken with structural engineers and a windsock manufacturer to test viability of the proposal. These tests concluded that the windsock pole could be on both lever-able arms to allow for long-term maintenance and a tapered approach would ensure stability.</p>
        <p>The Windsocks – Textile artist Alice Kettle and Joseph Ayavoro had planned phase 2 workshops to develop the proposals with local groups and students from the Black History course held at the University. Although the phase 2 workshops were not undertaken, the phase 1 visual of Alice Kettle’s design for the trial windsock can be seen in the images for the project.</p>
      </div>

