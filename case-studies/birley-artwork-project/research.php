      <h3 class="content-text__title">Research</h3>

      <p>The Birley Archive Project has an important place in the recording of this project as a feasibility and concept design study for public art and approaches to a complex site. In essence, it is a project output in its own right. The archive developed through graduate placements as well as the artists research. It is viewed as a platform for investigations into the history of the site and can be used as an educational and archive resource, as well as a portal for further exploration of the university’s digital resources.</p>
      <p>In asking the question: ‘What is Public Art?’ and ‘How does it relate to this site?’, the project coordinator and curator undertook widespread consultation to understand how the concept designs might be developed if they were to go ‘live’. Beyond the realisation of physical structures, the group had identified at an early stage that the key to ownership in any project was to create structure or platforms that could act as ‘enablers’ for future creativity. It is hoped that this project is one such enabler.</p>
      <p>The Living Lines project started as a means of connecting with community, staff and students at the recently occupied Birley site through a feasibility study of proposed art installations curated and devised by School of Art staff.</p>
      <p>The project focus was to uncover the hidden narratives of Birley and find new ways of engaging and communicating the subtexts of a site on which a new community (i.e. staff, students, building and site users) had formed as part of the many layers of the site’s history.</p>
      <p>The themes of the Birley Artwork project centred primarily around the duality of permanence and transience within the context of ‘place’, ‘being sited’ or ‘home'. Sustainability and history came through strongly in the pieces which aimed at connecting the past to the present and the present to an imagined future.</p>
      <p>As part of the research into the installations, sculptor Brigitte Jurack investigated the lives of people living in industrialised Manchester in the terraced street whose foundations remain buried beneath the current Birley site, resurrecting memories of the local history society in text (<a href="stories-from-the-hearth/">Stories From The Hearth</a>) which was to be etched onto the chimneys of the outline shape of a <a href="golden-terrace.php">Golden Terrace</a> house. The sense of community was reinforced by threshold steps people were encouraged to sit on and ‘chat’ like neighbours might.</p>

      <p><a href="where-the-wind-blows.php">Where the Wind Blows</a> developed from a proposal by landscape architect, Ian Fisher: ‘Reconnecting with Natural Processes’ for 10-metre high windsocks on a central lawn. Along with Ian’s own investigative practice, structural engineers, Renaissance, and windsock supplier Windsocks Direct developed and tested the viability of the proposals. Further consultation was initiated with researchers in Science and Engineering and the University’s biodiversity group over opportunities for weather monitoring. The design itself involved collaboration between sculptor Joseph Ayavoro and textile artist Alice Kettle. Alice created the textile design of the windsocks, whilst Joseph designed the drawings for a wave like compass at its base indicating the winds that led to an arrival to/or departure from the site, through waves of regeneration and different lives lived across the globe. A poem by SuAndi completed the project.</p>
      <p>Further designs were developed around sustainability themes: a ‘bee’ project, <a href="the-bees.php">The Bees</a>, with wicker hives and planting by artist Janet Griffiths, along with the potential for inclusive seating (<a href="terrace-seating.php">Terrace Seating</a>) by 3D Designer Dave Grimshaw, which was linked to the site history and context with proposed involvement by the Age Friendly initiative and Hulme Community Garden Centre.
      Although, following review, it was concluded that the site designs would not be taken forward as physical structures, the digital communication codes (<a href="thresholds.php">Thresholds</a>), which aimed to direct users to information that locates them within the context of the site, emerged in an evolved form as part of the CityVerve funded research project on the All Saints site. The project currently uses a physical object to transmit digital 3D images of objects in the Special Collections and has future potential for temporary installations at Birley.</p>


