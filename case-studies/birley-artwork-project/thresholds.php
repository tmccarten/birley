      <h3 class="content-text__title">Thresholds</h3>

      <p>The Thresholds project was conceptualised by the Head of the School of Architecture Professor Tom Jefferies and Professor for Design Research and textile artist Alice Kettle. The original project proposal adopted the creation of physical markers at ‘thresholds’ or entrances to the site. The portals evolved from ‘welcome mats’ in the form of QR codes, to circular imprints reminiscent of coal holes and early tribal tattoos, and later physical communication posts. These ‘portals’ had the potential to be located at other sites, creating a network of information about different sites and context and to connect to information contained in digital archives and physical collections.</p>
      <p>The Thresholds project was an early stage concept design in developing the Birley Archive Project and the creative platforms that were developed by the School of Architecture as part of the CityVerve Culture Research strand through the <a href="http://aboutmanchester.co.uk/2017/06/cityverve-project-to-transform-all-saints-park-into-virtual-art-gallery/">Plinth</a> in All Saints Park.</p>

      <h4>Project Proposal Summary</h4>
      <div class="serif-text">
        <p>As the doorway or entrance to Birley, to the Golden Terrace houses and the places to sit, the thresholds are crossed (over) as starting points to places / events as entries and exits from one place to another.</p>

        <p>The Thresholds might be viewed as doormats: walked over and scattered at ground level on the Birley site (or communication portals). They playfully and aesthetically mediate moving from one site, one place into another. Gold brass detail will link with the Golden Terrace house project.</p>

        <p>The codes will provide a distinctive surface pattern as crossing points. They will use as a starting point text contributions from community projects run by outreach projects supported by the University. [In this way] codes can also allow access to other information such as online blogs and web information or as a portal into archival information.</p>

        <p>These text contributions are words and sentences which will be incorporated into the structure as physical ‘tags’ or patterns, e.g. as made through a QR code generator.</p>

        <p>The structure allows for future contributions to be added as part of the pattern making and on-going narrative content of the work. This offers a continuous poem, storytelling cycle to evolve. It also allows for different combinations of sentences to form and generate through accessing the codes in a variety of sequences.</p>

        <h5>Detail</h5>
        <ul class="content-text__list">
          <li class="content-text__list-item"> -	Background research into which community/outreach/educational or sustainability projects could connect with the threshold project dependent on context and location.</li>
          <li class="content-text__list-item"> -	Investigate links with poetry competitions and Reelmcr’s documentary project</li>
          <li class="content-text__list-item"> -	Connections with digital archiving project and Special Collections, e.g. archaeological dig artefacts.</li>
          <li class="content-text__list-item"> -	Codes could visually represent a connection with the site.</li>
        </ul>

        <h5>Realisation</h5>
        <ul class="content-text__list">
          <li class="content-text__list-item"> -	Identify locations and possible risks and constraints. (Review with Estates, Health and Safety, Accessibility, etc.)</li>
          <li class="content-text__list-item"> -	Identify patterns according to links and code communication.</li>
          <li class="content-text__list-item"> -	Set out patterns for fabrication.</li>
          <li class="content-text__list-item"> -	Install communication posts, mats, codes, etc.</li>
          <li class="content-text__list-item"> -	Produce a location key and communication visuals</li>
        </ul>

