    <h3 class="content-text__title content-text__title--poems">Stories From The Hearth</h3>

    <div class="serif-text">
      <p>When I think of the hearth, I think of cats. Cats everywhere. One cat got in the oven. Wherever there was a fire there was a cat.</p>
      <p>I wasn’t allowed to sit by the fire. That was dad’s spot. One night he came back late, worse for wear. My mum gave him a plate of carrots for his tea. He threw them on the fire – plate and all. Children didn’t get to sit too close. There was a hierarchy for sitting round the fire. They told you tales as a child of what happened if you got too close. ‘You’ll get corn beef legs sitting so close to the fire!’ they would say.</p>
      <p>It was my job to clean the grates. The first to get up out of bed did the grate. I remember coal shovels - and  the ash. It was white, grey ash – dank and dusty. Ash got everywhere. It drove my mother mad. The living room would be covered in soot.</p>
      <p>Sometimes a pigeon would fall down the chimney into the fire grate. The chimneys were built at an angle, and stuff could get trapped. You’d have to go up there with a poker. Or the chimney soot would reignite and you’d have to clear the fire out.</p>
      <p>Of course, there wasn’t always a fire guard. It was mostly cast iron fireplaces in those days. And then there were hobs you could top load with coal.</p>
      <p>A lot of the time we couldn’t afford to start the fire up. We would go out looking for wood or get kindling sticks from the newsagents. We had a coal bunker and a coal scuttle. Sometimes you just got what fitted on the shovel. If you wanted to keep the fire going you could throw sugar on it. The fire took a while to die down though. It kept the rooms above warm for a while.</p>
      <p>It was mostly just the back room fire that was kept burning. They’d light an extra fire in your bedroom if you were sick. Or you might light the fire in the front room, the parlour fire, on special occasions like Christmas -  of if you had visitors.</p>
      <p>We used firelighters to start the fire, if you could get them, but mostly it was anything you could get hold of. Bits of newspaper (usually the Evening News) and kindling tied in knot. If we didn’t have enough coal, we would queue up for coke.</p>
      <p>Coke came in briquettes; they kept in the heat. Coke was cheaper. Mother used to send us out to fill the pram with it. There was a coke yard at Gaythorne Gas Works. The queues were so long they went round the block. Once I stood for three or four hours. I was with my friend Irene. I was frozen but I didn’t feel the cold the same way then. We were just having a laugh.</p>
      <p>We used to tell tales around the fire. When you heard the wind whistling up the chimney you’d think of ghosts - sometimes you’d say you’d seen the face of a banshee in the fire to frighten your brother or sister.</p>
      <p>We would toast crumpets – I made a fork once at school in metalwork. Devil type pitch forks they were. Then take a slice from an unsliced loaf.</p>
      <p>Another memory was the sky. The sky was black; it was thick with soot.  I thought that the black soot was the sky. Once I saw a bit of blue. I thought that that little pocket was all there was. I didn’t really believe the sky was blue.</p>
      <p>I still have a head for figures:</p>
      <p><i>Kindling – tuppence from Victoria Street<br>
      Coal – sold by the hundred-weight – 60 kilos – 10 litres - two bags a week</i>

      <p>Then there were tiles. Tiles were expensive, but once one person had them, word got round to the neighbours and everyone wanted them after that.</p>
      <p><i>12 shillings-6p a week. we paid a plumber in instalments to fit tiles – 1952</i></p>
      <p>There wasn’t ever much on the fireplace. Except the clock. I still remember the sound of the clock chiming when we got our first clock. Mind - in those days, you didn’t have all the things you do now to tell the time. Most people didn’t have a watch. Some people only knew the time by the church bell or the ‘knocker upper’. We got the clock on the tick.</p>
