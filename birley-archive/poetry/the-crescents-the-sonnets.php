    <h4>Sonnet I</h3>

      <p>of prayers and CMYK throughout the day<br>
      And noir: It is, I’m afraid, what’s happening<br>
      She’s flying through your neighbourhood.<br>
      Where concrete, bleeds<br>
      And courses through the streets in vein<br>
      I will turn the lights off.<br>
      With the loose grip and the last drip.<br>
      4.16am. she’s yours, she belongs to you.<br>
      still he wears his hips<br>
      And Idle, eyes don’t feel like jumping<br>
      Where tongues attack, the frog’s back<br>
      But there is no assisting this<br>
      in their territory, the pulse of the shrub, so tedious<br>
      the inquest into the death was cancelled on a technicality.</p>

    <h4>Sonnet II</h4>

      <p>We will have to wait until it has been formed.<br>
      Most of the space remained unfilled<br>
      And our space is steadily erasing<br>
      With fresh salt in the breeze,<br>
      a gust beneath the broadsheet.<br>
      Deep down the rabbit’s hole<br>
      trees suck the life out of this place,<br>
      And old passages recite themselves<br>
      With her back to the bricks.<br>
      Andrew loves Allanah.<br>
      But there is no assisting this<br>
      it is getting to know itself.</p>

      <p>Sometimes I think I should just go out and find that, that did this to him and</p>

      <p><i>Charles Fyfe Williams</i></p>




